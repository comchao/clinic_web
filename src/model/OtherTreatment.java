package model;

public class OtherTreatment {
	int id;
	String otherName;
	Double 	otherPrice;
	String otherUnit;
	String country ;
	String litres;
	String treatment_other_total;
	String visits;
	public String getVisits() {
		return visits;
	}
	public void setVisits(String visits) {
		this.visits = visits;
	}
	public String getTreatment_other_total() {
		return treatment_other_total;
	}
	public void setTreatment_other_total(String treatment_other_total) {
		this.treatment_other_total = treatment_other_total;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getLitres() {
		return litres;
	}
	public void setLitres(String litres) {
		this.litres = litres;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOtherName() {
		return otherName;
	}
	public void setOtherName(String otherName) {
		this.otherName = otherName;
	}
	public Double getOtherPrice() {
		return otherPrice;
	}
	public void setOtherPrice(Double otherPrice) {
		this.otherPrice = otherPrice;
	}
	public String getOtherUnit() {
		return otherUnit;
	}
	public void setOtherUnit(String otherUnit) {
		this.otherUnit = otherUnit;
	}
	

}
