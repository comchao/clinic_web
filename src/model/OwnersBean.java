package model;

public class OwnersBean {
	private int id;
	private String owners_idcard;
	private String prefix;
	private String owners_name;
	private String owners_lname;
	private String owners_gender;
	private String address;
	private String telephon;
	private String owners_birthday;
	
	public String getPrefix() {
		return prefix;
	}
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}
	public String getOwners_name() {
		return owners_name;
	}
	public void setOwners_name(String owners_name) {
		this.owners_name = owners_name;
	}
	public String getOwners_lname() {
		return owners_lname;
	}
	public void setOwners_lname(String owners_lname) {
		this.owners_lname = owners_lname;
	}
	public String getOwners_gender() {
		return owners_gender;
	}
	public void setOwners_gender(String owners_gender) {
		this.owners_gender = owners_gender;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getTelephon() {
		return telephon;
	}
	public void setTelephon(String telephon) {
		this.telephon = telephon;
	}
	public String getOwners_birthday() {
		return owners_birthday;
	}
	public void setOwners_birthday(String owners_birthday) {
		this.owners_birthday = owners_birthday;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getOwners_idcard() {
		return owners_idcard;
	}
	public void setOwners_idcard(String owners_idcard) {
		this.owners_idcard = owners_idcard;
	}
	

}
